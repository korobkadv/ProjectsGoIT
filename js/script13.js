/*
Функція isAdult оголошує один параметр age (вік), значення якого буде задаватися під час її виклику. 
Присвой змінній passed вираз перевірки віку користувача на повноліття. Людина вважається повнолітньою 
у віці 18 років і старше.


*/

function isAdult(age) {
  const passed = age >= 18;
  return passed;
}

isAdult(20); // повертає true
isAdult(14); // повертає fals
